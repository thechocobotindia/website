  window.PRODUCTS.filter(p => p.category === "signature");
window.viralProduct = () =>
  window.PRODUCTS.find(p => p.viral);
window.formatPrice = (n) =>
  new Intl.NumberFormat("en-IN", { style: "currency", currency: "INR", maximumFractionDigits: 0 }).format(n);
