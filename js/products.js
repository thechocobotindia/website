window.findProduct = (id) => window.PRODUCTS.find(p => p.id === id);
window.productsByCategory = (catId) =>
  (!catId || catId === "all") ? window.PRODUCTS : window.PRODUCTS.filter(p => p.category === catId);
window.featuredProducts = (count = 4) =>
  window.PRODUCTS.filter(p => p.bestseller).slice(0, count);
